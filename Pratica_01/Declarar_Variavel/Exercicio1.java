package Pratica_01.Declarar_Variavel;

public class Exercicio1 {

    public static int variavelInt = 50;
    public static double variavelDouble = 50.0;
    public static float variavelFloat = 50.0f;
    public static long variavelLong = 50_000_000_000L;
    public static boolean variavelBoolean = false;

    public static void main(String[] args) {
        System.out.println("Valor int " + variavelInt);
        System.out.println("Valor Double " + variavelDouble);
        System.out.println("Valor Float " + variavelFloat);
        System.out.println("Valor int " + variavelLong);
        System.out.println("Valor int " + variavelBoolean);




    }
}
